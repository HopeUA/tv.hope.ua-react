/**
 * [IL]
 * Library Import
 */
import React, { Component, PropTypes } from 'react';

/**
 * [IV]
 * View Import
 */
import Common from './View/Common/tpl';
import { connect } from 'react-redux';

/**
 * [IBP]
 * Pixel Perfect and Breakpoints
 */
import PixelPerfect from 'vendor/PixelPerfect/component';

/**
 * [ICONF]
 * Config Import
 */
import config from './config';

@connect((state) => {
    return {
        mediaType: state.browser.mediaType
    };
})
class WorldNetwork extends Component {
    static propTypes = {
        mediaType: PropTypes.string.isRequired
    };

    /**
     * [CDN]
     * Component display name
     */
    static displayName = config.id;

    /**
     * [CR]
     * Render function
     */
    render = () => {
        const { mediaType } = this.props;

        /**
         * [RCD]
         * Config destructuring
         */
        const { id } = config;

        /**
         * [RV]
         * View
         */
        const view = (<Common mediaType={ mediaType }/>);

        /**
         * [RR]
         * Return Component
         */
        return (
            <PixelPerfect component={ id }>
                { view }
            </PixelPerfect>
        );
    }
}

/**
 * [IE]
 * Export
 */
export default WorldNetwork;
